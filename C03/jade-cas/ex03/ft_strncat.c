char	*ft_strcat(char *dest, char *src, unsigned int nb)
{
	int i;

	i = 0;
	while (dest[i] != '\0')
	{
		dest[i] = '\0';
		i++;
	}
	while (src[i] <= nb)
	{
		i = 0;
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

